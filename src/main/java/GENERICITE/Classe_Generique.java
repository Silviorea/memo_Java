package GENERICITE;

public class Classe_Generique <Type> 
{
	
	private Type element;
	

	
	public Classe_Generique(Type element) {
		super();
		this.element = element;
	}

	
	

	public Type getElement() {
		return element;
	}


	public void setElement(Type element) {
		this.element = element;
	}
	
	
	
	

}
